typealias FoodDictionary = ([String : Any])

//**************************************************************************************************
//
// MARK: - Struct -
//
//**************************************************************************************************

class FoodVO {

//*************************************************
// MARK: - Properties
//*************************************************

    var category: String?
    var name: String?
    var price: Double?
    var image: String?
    var quantity: Int?
    
//*************************************************
// MARK: - Constructors
//*************************************************
    
    init() {
        
    }
    
    init(food: FoodDictionary) {
        self.category = food["category"] as? String
        self.name = food["name"] as? String
        self.price = food["price"] as? Double
        self.image = food["image"] as? String
        self.quantity = 0
    }

//*************************************************
